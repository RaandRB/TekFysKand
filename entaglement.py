def entanglement(a):
    d1 = a[0]**2*a[7]**2+a[1]**2*a[6]**2+a[2]**2*a[5]**2+a[3]**2*a[4]**2
    d2 = a[0]*a[7]*a[3]*a[4]+a[0]*a[7]*a[5]*a[2]+a[0]*a[7]*a[6]*a[1]+a[3]*a[4]*a[5]*a[2]+a[3]*a[4]*a[6]*a[1]+a[5]*a[2]*a[6]*a[1]
    d3 = a[0]*a[6]*a[5]*a[3]+a[7]*a[1]*a[2]*a[4]

    svar = 4*abs(d1-2*d2+4*d3)
    return svar
